#while condition checking using square function task# 
import turtle
Om_turtle=turtle.Turtle()
def square():
  Om_turtle.forward(100)
  Om_turtle.right(90)
  Om_turtle.forward(100)
  Om_turtle.right(90)
  Om_turtle.forward(100)
  Om_turtle.right(90)
  Om_turtle.forward(100)

Elephant_weight=1000
Ant_weight=0.15
#applying while loop  #

#applying when condition is false #
while Elephant_weight<Ant_weight:
  square()
else:
  Om_turtle.forward(100)
